		<p>
		   Taco Truck is the premier place for great tacos at an affordable price, with many locations scattered around the Dallas metropolitan area. But as with most great food establishments, Taco Truck has very humble beginnings.

</br></br>Started in the late 1950's by a man by the name of Rodrigo Juevara, Taco Truck was the first mobile dining company in Dallas. Rodrigo was the 4th child, and 1st son, of his immigrant parents Alexander and Marie Juevara. Since his birth, Rodrigo's father always encouraged Rodrigo to focus on his studies and education, often renting books from the library for Rodrigo even though his own English was lacking. Rodrigo followed his father's guidance, and very quickly had a mastery of the English language at the young age of 8. 

</br></br>Fast-forward to Rodrigo's teenage years, Rodrigo has been accepted to Southern Methodist University in Dallas. His father was extremely proud of his son, but sadly passed away shortly after his son's acceptance. With no way to pay for his education, Rodrigo decided that he would sell authentic Mexican food to support him and his family. With recipes from his mother and grandmother, Rodrigo began to cook constantly. He took to cooking much like he took to learning.

</br></br>Rodrigo eventually sold enough of his food, and got enough praise, for him to be able to afford to buy a truck. He wanted to be able to be anywhere selling his food. So that's what he did. Today, he owns 5 different trucks, and all of them do extremely well. Sadly, he can no longer cook inside the trucks himself due to arthritis, but he does occasionally make tacos for himself and his wife. To this day, Rodrigo has tried to learn something new everyday, and is still very much an active reader. 

</br></br>So when you buy food from Taco Truck, know that you aren't buying food from a giant corporate machine that doesn't care about its customers. So have yourself a taco, and enjoy!
